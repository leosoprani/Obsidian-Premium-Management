async function generate() {
    for (const [name, svgContent] of Object.entries(iconSvgs)) {
        // Generate Active
        const activeSvg = svgContent.replace('currentColor', colors.active);
        await sharp(Buffer.from(activeSvg))
            .resize(128, 128)
            .png()
            .toFile(path.join(ICONS_DIR, `${name}_active.png`));
            
        // Generate Inactive
        const inactiveSvg = svgContent.replace('currentColor', colors.inactive);
        await sharp(Buffer.from(inactiveSvg))
            .resize(128, 128)
            .png()
            .toFile(path.join(ICONS_DIR, `${name}_inactive.png`));

        // Generate White (for some buttons)
        const whiteSvg = svgContent.replace('currentColor', colors.white);
        await sharp(Buffer.from(whiteSvg))
            .resize(128, 128)
            .png()
            .toFile(path.join(ICONS_DIR, `${name}_white.png`));
            
        console.log(`Generated icons for: ${name}`);
    }
}

generate().catch(console.error);
